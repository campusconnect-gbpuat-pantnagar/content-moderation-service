  private readonly similarityThreshold = 0.8;

  public levenshtein(a: string, b: string): number {
    const an = a.length;
    const bn = b.length;
    if (an === 0) return bn;
    if (bn === 0) return an;

    const matrix = [];
    for (let i = 0; i <= bn; i++) {
      matrix[i] = [i];
    }
    for (let j = 0; j <= an; j++) {
      matrix[0][j] = j;
    }

    for (let i = 1; i <= bn; i++) {
      for (let j = 1; j <= an; j++) {
        if (b.charAt(i - 1) === a.charAt(j - 1)) {
          matrix[i][j] = matrix[i - 1][j - 1];
        } else {
          matrix[i][j] = Math.min(
            matrix[i - 1][j - 1] + 1, // substitution
            Math.min(
              matrix[i][j - 1] + 1, // insertion
              matrix[i - 1][j] + 1,
            ),
          ); // deletion
        }
      }
    }
    return matrix[bn][an];
  }

  public similarity(a: string, b: string): number {
    const distance = this.levenshtein(a, b);
    return 1 - distance / Math.max(a.length, b.length);
  }
  public categorizeText(text: string): { categories: string[]; score: number } {
    // Tokenize and normalize the text
    const words = text.toLowerCase().split(/\W+/);

    // Initialize category scores
    const scores = Object.keys(this.categoryScores).reduce(
      (acc, category) => {
        acc[category] = 0;
        return acc;
      },
      {} as { [key: string]: number },
    );

    // Match words with similarity check and calculate raw scores
    words.forEach((word) => {
      for (const category in this.categoryWords) {
        this.categoryWords[category].forEach((categoryWord) => {
          const similarityScore = this.similarity(word, categoryWord);
          if (similarityScore >= this.similarityThreshold) {
            scores[category] += this.categoryScores[category] * similarityScore;
          }
        });
      }
    });

    // Normalize scores between 0 and 1
    const normalizedScores = Object.keys(scores).reduce(
      (acc, category) => {
        acc[category] = scores[category] / this.categoryScores[category];
        return acc;
      },
      {} as { [key: string]: number },
    );

    // Determine categories with scores greater than 0
    const matchedCategories = Object.keys(normalizedScores).filter(
      (category) => normalizedScores[category] > 0,
    );

    // Calculate the overall safety score (1 - highest normalized score)
    const maxNormalizedScore = Math.max(...Object.values(normalizedScores));
    const safetyScore = 1 - maxNormalizedScore;

    return { categories: matchedCategories, score: safetyScore };
  }
